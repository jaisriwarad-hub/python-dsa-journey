# Day 03 - Operators and Conditions

# Arithmetic Operators
a = 10
b = 3

print(a + b)   # addition
print(a - b)   # subtraction
print(a * b)   # multiplication
print(a / b)   # division
print(a % b)   # modulus

# Comparison Operators
print(a > b)
print(a < b)
print(a == b)
print(a != b)

# Logical Operators
x = True
y = False

print(x and y)
print(x or y)
print(not x)

# Conditional Statements
age = 18

if age >= 18:
    print("Eligible to vote")
else:
    print("Not eligible to vote")